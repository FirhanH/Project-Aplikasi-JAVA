    private void loadTable() {
        List<Motor> lsMOTOR = new ArrayList<Motor>();
        ControllerMotor cmdMt = new ControllerMotor();
        lsMOTOR = cmdMt.getAllMotor();

        String[][] dtMt = new String[lsMOTOR.size()][4];
        int i = 0;
        for (Motor mt: lsMOTOR){
            dtMt[i][0] = mt.getIdMotor();
            dtMt[i][1] = mt.getNama();
            dtMt[i][2] = mt.getMerk();
            dtMt[i][3] = ""+mt.getHarga();

            i++;
        }
        
    }
    
