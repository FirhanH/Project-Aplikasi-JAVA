    private void txtcektotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtcektotalActionPerformed
        // Mengambil nilai dari JTextField dan JSpinner

        int banyak = Integer.parseInt(spinjumlah.getValue().toString());
        int harga = Integer.parseInt(txtharga.getText());

        // Menghitung jumlah
        int jumlah = banyak * harga;
        Motor mt = new Motor();
        mt.setIdMotor(txtidmotor.getText());
        mt.setHarga(jumlah);
   

        int totalharga = jumlah;

        // Membuat objek NumberFormat dengan format mata uang Indonesia (Rupiah)

        // Mengatur nilai ke JTextField dengan format rupiah
        txttotal.setText(String.valueOf(totalharga));
    }//GEN-LAST:event_txtcektotalActionPerformed

    private void txtbeliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtbeliActionPerformed
        int uangPembelian = Integer.parseInt(txtbayar.getText());
        int hargaTotal = Integer.parseInt(txttotal.getText());

        int bayar = uangPembelian - hargaTotal;

        if (bayar < 0) {
            JOptionPane.showMessageDialog(null, "Uang anda tidak cukup: " );
        } else {
            txtkembalian.setText(String.valueOf(formatRupiah.format(bayar)));

            String waktu = txttanggal.getText();
            String idmotor = txtidmotor.getText();
            String nama = txtnama.getText();
            String merk = txtnama.getText();
            int jumlah = (int) spinjumlah.getValue();
            int totalharga = Integer.parseInt(txttotal.getText());

            ControllerTransaksi cmdTransaksi = new ControllerTransaksi();
            Transaksi transaksi = new Transaksi(waktu, username, idmotor, nama, merk, jumlah, totalharga);
            int hasil = cmdTransaksi.simpanTransaksi(transaksi);
            if (hasil > 0) {
                JOptionPane.showMessageDialog(null, "Transaksi berhasil");
                // Lakukan tindakan setelah registrasi berhasil
            } else {
                JOptionPane.showMessageDialog(null, "Transaksi gagal");
                // Lakukan tindakan setelah registrasi gagal
            }

        }
    }//GEN-LAST:event_txtbeliActionPerformed

